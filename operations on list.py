#operations to perform on list in python

a = [1,2,3,4,5,6,7,8]

print(a.index(5))

print(a[0:5])
print(a[-4:-1])

print(sum(a))

#count occurence of a particular
print(a.count(1))

#length of a list
print(len(a))

#reverse of a list
a.reverse()
print(a)


print(min(a))

print(max(a))


